
def area(a):
    """
    Возвращает площадь квадрата.

    Args:
        a (int): Длина стороны квадрата.

    Returns:
        int: Площадь квадрата.

    Example:
        >>> area(4)
        16
    """
    return a * a


def perimeter(a):
    """
    Возвращает периметр квадрата.

    Args:
        a (int): Длина стороны квадрата.

    Returns:
        int: Периметр квадрата.

    Example:
        >>> perimeter(4)
        16
    """
    return 4 * a
